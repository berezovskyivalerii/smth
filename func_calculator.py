class Calculator:
    def add(self, a, b):
        return a + b
    
    def substract(self, a, b):
        return a - b

    def devide(self, a, b):
        return a / b
    
    def multiply(self, a, b):
        return a * b

    def max(self, a, b):
        return max(a, b)
    
    def min(self, a, b):
        return min(a, b)

    def percentage(self, a, b):
        return a * b / 100
    
    def exp(self, a, b):
        return a ** b